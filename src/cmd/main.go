package main

/*
	Модуль определеяет точку входа всег оприложения
*/

import (
	_ "github.com/urfave/cli/v2"
	"log"
	мПрилож "oc/app"
	мПарам "oc/app/hardparam"
)

func main() {
	парам := &мПарам.ТПарам{
		Версия: "0.0.2",
		Сборка: "0007",
		Дата:   "2020-07-17 21:36:51",
	}

	прилож, ош := мПрилож.Нов()
	switch ош == nil {
	case true:
		прилож.Пуск(парам)
	default:
		log.Panicf("main(): АВАРИЯ при запуске приложения\n\t%v", ош)
	}

}
