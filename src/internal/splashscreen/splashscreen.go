package splashscreen

/*
	Модуль предоставляет шапку для вывода на экран
*/

import (
	"fmt"
	мПарам "oc/internal/app/param"
)

//Печать -- выводит шапку в начале работы
func Печать(парам *мПарам.ТПарам) {
	fmt.Printf("\t\t/===========================\\\n")
	fmt.Printf("\t\t|    Оберон-компилятор      |\n")
	fmt.Printf("\t\t| Версия:%v Сборка:%v  |\n", парам.Версия, парам.Сборка)
	fmt.Printf("\t\t| Дата:%v  |\n", парам.Дата)
	fmt.Printf("\t\t\\===========================/\n")
}
