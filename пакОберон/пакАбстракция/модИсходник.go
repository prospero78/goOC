package пакАбстракция

/*
	Модуль предоставляет абстракцию для исходного текста
*/

//СИсхРазмер -- специальный целочисленный тип для хранения размера исходника
type СИсхРазмер int

//СИсхТекст -- специальный строковый тип для хранения исходного текста
type СИсхТекст string

//СИсхФайл -- специальный строковый тип для имени файла исходника
type СИсхФайл string

//АИсхФайл -- абстракция для исходного файла
type АИсхФайл interface {
	Считать(СИсхФайл) error
	Исходник() СИсхТекст
	Лит(СЛитНомер) (СЛит, error)
	Размер() СИсхРазмер
}

//СИсхСтрока -- специальный строковый тип для хранения исходника строки
type СИсхСтрока string

//АИсхСтроки -- абстракция для исходника разбитого построчно
type АИсхСтроки interface {
	НаСтрокиРазбить(СИсхТекст)
	Строка(СКоордСтр) СИсхСтрока
}

//АИсходник -- абстракция для исходника
type АИсходник interface {
	Обработать(СИсхФайл) error
	Слова() map[int]АСлово
}
