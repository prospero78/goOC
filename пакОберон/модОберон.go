package пакОберон

/*
	Пакет предоставляет тип компилятора Оберона для главного файла
*/

import (
	пакКомпилятор "./пакКомпилятор"
)

type ТуОберон struct {
	Компилер пакКомпилятор.ТуКомпилятор
	рес      byte
	гуи      byte
	конс     byte
}

// Создаёт новый экземпляр компилятора
func Новый() (оберон *ТуОберон) {
	оберон = new(ТуОберон)
	return оберон
}

// Начинает выполнение компилятора
func (ТуОберон) Выполнить() {
	врем := 0
	врем += 1
}
