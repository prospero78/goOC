package пакКоорд

/*
	Тестирует ТКоордФиксСтр
*/

import (
	мТест "testing"
)

func TestКоордФиксСтрНов(тест *мТест.T) {
	_Позитив := func() {
		поз, ош := КоордФиксСтрНов(10)
		if ош != nil {
			тест.Errorf("п1 Ошибка при создании ТКоордФиксСтр\n\t%v", ош)
		}
		if поз.Знач() != 10 {
			тест.Errorf("п2 Ошибка при хранении значения позиции. Должно быть 10, знач=%v", поз.Знач())
		}
	}
	_Негатив := func(){
		_, ош := КоордФиксСтрНов(0)
		if ош == nil {
			тест.Errorf("н1 Нет ошибки при создании ТКоордФиксПоз\n\t%v", ош)
		}
	}
	_Позитив()
	_Негатив()
}
